# Declare a string variable with any value
my_string = "Hello, World!"

# Print out the string in uppercase form
print("Uppercase:", my_string.upper())

# Experiment with other string functions

# Print out the string in lowercase form
print("Lowercase:", my_string.lower())

# Print out the string with the first letter of each word capitalized
print("Title Case:", my_string.title())

# Print out the string with leading and trailing whitespace removed
print("Stripped:", my_string.strip())

# Print out the string with a substring replaced
print("Replace 'World' with 'Python':", my_string.replace("World", "Python"))

# Print out the length of the string
print("Length of the string:", len(my_string))

# Print out the string with each word capitalized
print("Capitalize:", my_string.capitalize())

# Print out whether the string starts with "Hello"
print("Starts with 'Hello':", my_string.startswith("Hello"))

# Print out whether the string ends with "!"
print("Ends with '!':", my_string.endswith("!"))
